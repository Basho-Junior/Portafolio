    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        if(jTextField1.getText().equals("") || jTextField2.getText().equals("") || jTextField3.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Llene de forma correcta todos lo campos");
        }else{
        String m = jTextField1.getText();
        String n = jTextField2.getText();
        String p = jTextField3.getText();
        Object[] row = { m, n, p};
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.addRow(row);
        if(model.getRowCount() > 7 ){
            JOptionPane.showMessageDialog(null, "No se permiten mas de 7 productos");
            model.setRowCount(0);
        }
        }
    
    }//GEN-LAST:event_jButton1MouseClicked

