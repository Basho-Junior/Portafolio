    public class Alumno {

        private String nombre, direccion;
        private int matricula;

        public Alumno(String nombre, String direccion, int matricula) {
            this.nombre = nombre;
            this.direccion = direccion;
            this.matricula = matricula;
        }

        public String getNombre() {
            return nombre;
        }

        public void setNombre(String nombre) {
            this.nombre = nombre;
        }

        public String getDireccion() {
            return direccion;
        }

        public void setDireccion(String direccion) {
            this.direccion = direccion;
        }

        public int getMatricula() {
            return matricula;
        }

        public void setMatricula(int matricula) {
            this.matricula = matricula;
        }
    }

    public class CustomListModel extends AbstractListModel {

        private ArrayList Lista = new ArrayList<>();

        @Override
        public int getSize() {
            return Lista.size();
        }

        @Override
        public Object getElementAt(int index) {
            Alumno a1 = (Alumno) Lista.get(index);
            return a1.getMatricula() + " " + a1.getNombre() + " " + a1.getDireccion();
        }

        public void agregarValores(Alumno a1) {
            Lista.add(a1);
            this.fireIntervalAdded(this, getSize(), getSize() + 1);
        }

        public void quitarValores(int index) {
            Lista.remove(index);
            this.fireIntervalRemoved(index, getSize(), getSize() + 1);
        }

        public Alumno getPersona(int index) {
            return (Alumno) Lista.get(index);
        }
    }

